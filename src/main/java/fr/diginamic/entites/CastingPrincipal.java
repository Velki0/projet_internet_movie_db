package fr.diginamic.entites;

/**
 * La classe CastingPrincipal représente chaque acteur du film qui possède un rôle principal dans un film.
 */
public class CastingPrincipal {

    /** L'id de l'acteur jouant un rôle principal au sein d'un film. */
    private String  id;
    /** L'identité de l'acteur jouant un rôle principal au sein d'un film. */
    private String identite;

    /**
     * Getter de l'id de l'acteur jouant un rôle principal.
     * @return l'id de l'acteur.
     */
    public String  getId() { return id; }

}
